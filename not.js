let  bloqueado = false;
        
if (!bloqueado) {
        console.log("Acesso liberado!");
}
        

/*
Descrição do exercício sobre acesso: 

Esse exercício tem como objetivo praticar o uso do operador lógico NOT (!).
O programa cria uma variável chamada "bloqueado" e define como false.
Depois, ele verifica se o usuário NÃO está bloqueado.

O operador NOT (!) serve para inverter o valor lógico.
Ou seja, se for true vira false, e se for false vira true.

Como a variável está como false, ao usar !bloqueado
ela se torna true, então o acesso é liberado.1
*/